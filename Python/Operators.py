# Arithmetic operators
# +,-,*,/,//,%,**
a = 10;
b = 5;
print(a+b)
print(a-b)
print(a*b)
print(a/5) # returns float value
print(a//b) # returns int value
print(a**b)

# Assignment operator
# =
txt = "Hello World"

# Comparison operator
# >,<,>=,<=,==,!=
print(a<b)
print(a>b)
print(a<=b)
print(a>=b)
print(a==10)
print(b!=10)

print("********")

# Logical Operators
# and, or, not
print(a>5 and b<5)
print(a>5 or b<5)
print(not(a>5 or b<5))

print("********")

# Identity Operators
# is, is not
print(a is b)
print(a is not b)

print("********")

# Membership Operators
# in, not in
msg = "Just chill in life"
print("Just" in msg)
print("Just" not in msg)

# Bitwise Operator
# &, |, ^, ~, <<, >>
print(a<<2)